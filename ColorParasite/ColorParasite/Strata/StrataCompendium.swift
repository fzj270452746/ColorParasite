import CoreGraphics

final class StrataCompendium {

    static let shared = StrataCompendium()

    private init() {}

    func blueprint(for index: Int) -> StratumBlueprint {
        guard index >= 1 && index <= allBlueprints.count else {
            return allBlueprints[0]
        }
        return allBlueprints[index - 1]
    }

    var totalCount: Int { return allBlueprints.count }

    private lazy var allBlueprints: [StratumBlueprint] = fabricateAllStrata()

    private func fabricateAllStrata() -> [StratumBlueprint] {
        var strata: [StratumBlueprint] = []
        strata.append(contentsOf: neophyteStrata())
        strata.append(contentsOf: adeptStrata())
        strata.append(contentsOf: arduousStrata())
        strata.append(contentsOf: crucibleStrata())
        return strata
    }

    // MARK: - Helper

    private func spec(_ id: String, _ variant: CorpuscleVariant, _ chroma: ChromaType?, _ x: CGFloat, _ y: CGFloat,
                      wormhole: String? = nil, waypoints: [CGPoint]? = nil) -> CorpuscleSpec {
        return CorpuscleSpec(identifier: id, variant: variant, chroma: chroma,
                             latticePosition: CGPoint(x: x, y: y),
                             wormholePartner: wormhole, nomadicWaypoints: waypoints)
    }

